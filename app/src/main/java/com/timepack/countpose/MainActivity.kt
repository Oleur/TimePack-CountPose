package com.timepack.countpose

import android.os.Bundle
import androidx.activity.compose.setContent
import androidx.activity.viewModels
import androidx.appcompat.app.AppCompatActivity
import androidx.compose.animation.ExperimentalAnimationApi
import androidx.compose.material.MaterialTheme
import androidx.compose.material.Surface
import com.timepack.countpose.home.HomeScreen
import com.timepack.countpose.theme.MyTheme

@ExperimentalAnimationApi
class MainActivity : AppCompatActivity() {

    private val timePackViewModel: TimePackViewModel by viewModels()

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MyTheme {
                Surface(color = MaterialTheme.colors.background) {
                    HomeScreen(timePackViewModel)
                }
            }
        }

        timePackViewModel.setupInitTime(10)
    }
}
